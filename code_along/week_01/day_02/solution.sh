Answer: 833 figs.

 1. Make 3 trips of 1,000 files 333 miles.
    There will be left with 2,001 figs and 667 miles to go.

 2. Take 2 trips of 1,000 figs 500 miles.
    There will be left with 1,000 figs and 167 miles to go.
